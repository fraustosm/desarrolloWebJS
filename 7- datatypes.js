// Data types in JavaScript

// JavaScript has dynamic typing, meaning variables can hold any type of data and can change types.
let number = 42; // Number type
let text = "Hello, World!"; // String type
let isTrue = true; // Boolean type
let nothing = null; // Null type
let notDefined; // Undefined type
let obj = { name: "Alice", age: 30 }; // Object type
let arr = [1, 2, 3]; // Array type
let bigIntValue = BigInt(123456789012345678901234567890); // BigInt type
let symbolValue = Symbol("unique"); // Symbol type
let dateValue = new Date(); // Date type
let regexValue = /abc/; // RegExp type
let funcValue = function() { return "I am a function"; }; // Function type